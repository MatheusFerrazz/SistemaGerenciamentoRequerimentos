package br.edu.ifrn.sgr.modelos;

/**
 *
 * @author CH
 */
public class Turma {
    private int codigo;
    private Curso curso;

    public Turma(int codigo, Curso curso) {
        this.codigo = codigo;
        this.curso = curso;
    }

    
    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the curso
     */
    public Curso getCurso() {
        return curso;
    }

    /**
     * @param curso the curso to set
     */
    public void setCurso(Curso curso) {
        this.curso = curso;
    }
    
   
}
